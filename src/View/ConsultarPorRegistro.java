    private void jButtonConsultarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonConsultarActionPerformed
        TarefaDao d = new TarefaDao();
        
        List<Tarefa> taref = d.listRC(jTextFieldConsultaRegistro.getText(), 1);
        

        if (taref.isEmpty()) {
            JOptionPane.showMessageDialog(null, "Campo vazio ou c칩digo inexistente!.", "Erro", JOptionPane.ERROR_MESSAGE);
            jTextFieldConsultaRegistro.setText("");
            jTextFieldConsultaRegistro.requestFocus();
        }
        String texto = "";
        for (Tarefa t : taref) {
            texto = texto + t.retornaTarefa();
        }
        jTextAreaConsultar.setText(texto);
    }//GEN-LAST:event_jButtonConsultarActionPerformed

